package Ptaki;

public class Ptak {
    String gatunek;
    String kolor;
    String wystepowanie;
    boolean czytworzygnazda;
    String dieta;

    Ptak (String gatunek, String kolor, String wystepowanie, boolean czytworzygnazda, String dieta)
    {
        this.gatunek = gatunek;
        this.kolor = kolor;
        this.wystepowanie = wystepowanie;
        this.czytworzygnazda = czytworzygnazda;
        this.dieta= dieta;
    }
}
